    private void startBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startBActionPerformed
        // TODO add your handling code here:
        if (blink) {
            stopB.doClick();
        }

        startB.setEnabled(false);
        stopB.setEnabled(true);
        operB.setEnabled(true);
        trafficLight1.setRedlight(Color.RED);
        trafficLight1.setGreenlight(initColor);
        trafficLight1.setYellowlight(initColor);
        trafficLight1.repaint();
        timer = new Timer();

        taskGreen2Yellow = new tLight_Task(initColor, Color.YELLOW, initColor, trafficLight1);
        taskYellow2Red = new tLight_Task(Color.RED, initColor, initColor, trafficLight1);
        taskRed2Green = new tLight_Task(initColor, initColor, Color.GREEN, trafficLight1);
        taskGreen2Yellow.setNextTask(taskYellow2Red, 1000);
        taskYellow2Red.setNextTask(taskRed2Green, 2000);
        taskRed2Green.setNextTask(taskGreen2Yellow, 3000);

        timer.schedule(taskRed2Green, 2000);


    }//GEN-LAST:event_startBActionPerformed

    private void stopBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stopBActionPerformed
        // TODO add your handling code here:
        startB.setEnabled(true);
        stopB.setEnabled(false);
        operB.setEnabled(false);
        blink = false;
        trafficLight1.setRedlight(initColor);
        trafficLight1.setGreenlight(initColor);
        trafficLight1.setYellowlight(initColor);
        taskRed2Green.cancelNextTask();
        taskGreen2Yellow.cancelNextTask();
        taskYellow2Red.cancelNextTask();
        blinkOff.cancelNextTask();
        blinkOn.cancelNextTask();
        timer.cancel();
        trafficLight1.repaint();
        

    }//GEN-LAST:event_stopBActionPerformed

    private void operBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_operBActionPerformed
        // TODO add your handling code here:
        stopB.doClick();
        blink = true;
        startB.setEnabled(true);
        stopB.setEnabled(true);
        operB.setEnabled(false);
        timer = new Timer();
        blinkOn = new tLight_Task(initColor, Color.YELLOW, initColor, trafficLight1);
        blinkOff = new tLight_Task(initColor, initColor, initColor, trafficLight1);
        blinkOn.setNextTask(blinkOff, 500);
        blinkOff.setNextTask(blinkOn, 500);

        timer.schedule(blinkOn, 500);

    }//GEN-LAST:event_operBActionPerformed

